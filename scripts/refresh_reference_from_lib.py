"""Refresh ``.figma_debug/reference/<feature>_screen.dart`` from committed ``lib/`` sources."""

from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path

from figma_flutter_agent.debug.dart_bundle import build_planned_dart_bundle
from figma_flutter_agent.generator.layout.common import to_pascal_case
from figma_flutter_agent.generator.paths import screen_file_path
from figma_flutter_agent.generator.pubspec import read_pubspec_name

_IMPORT_RE = re.compile(r"^import\s+'package:([^/]+)/([^']+)';\s*$", re.MULTILINE)


def _canonical_screen_source(
    feature_name: str,
    *,
    package_name: str,
    max_web_width: int = 1200,
) -> str:
    """Return a valid screen file that delegates to ``{Feature}Layout`` (spec §7)."""
    screen_class = f"{to_pascal_case(feature_name)}Screen"
    layout_class = f"{to_pascal_case(feature_name)}Layout"
    return f"""// <auto-generated>
// Generated by figma-flutter-agent reference maintainer. Do not edit by hand.
// </auto-generated>

// <custom-code>
// </custom-code>

/// Responsive shell injected by the generator for web/tablet max width.

import 'package:flutter/material.dart';
import 'package:{package_name}/theme/app_layout.dart';
import 'package:{package_name}/generated/{feature_name}_layout.dart';

class GeneratedScreenShell extends StatelessWidget {{
  const GeneratedScreenShell({{
    super.key,
    required this.child,
    this.maxWebWidth = {max_web_width},
  }});

  final Widget child;
  final double maxWebWidth;

  @override
  Widget build(BuildContext context) {{
    final layout = Theme.of(context).extension<AppLayoutExtension>();
    final resolvedMaxWidth = layout?.maxWebWidth ?? maxWebWidth;
    return LayoutBuilder(
      builder: (context, constraints) {{
        final width = constraints.maxWidth;
        final horizontalPadding = AppBreakpoints.horizontalPadding(width);
        final contentMaxWidth = AppBreakpoints.contentMaxWidth(
          width,
          resolvedMaxWidth,
        );
        final Widget body = Align(
          alignment: Alignment.topCenter,
          child: ConstrainedBox(
            constraints: BoxConstraints(maxWidth: contentMaxWidth),
            child: Padding(
              padding: EdgeInsets.symmetric(horizontal: horizontalPadding),
              child: child,
            ),
          ),
        );
        return body;
      }},
    );
  }}
}}

class {screen_class} extends StatelessWidget {{
  const {screen_class}({{super.key}});

  @override
  Widget build(BuildContext context) {{
    return GeneratedScreenShell(child: const {layout_class}());
  }}
}}
"""


def _collect_widget_planned_files(
    package: str,
    project_dir: Path,
    sources: list[str],
) -> dict[str, str]:
    """Collect ``lib/widgets`` bodies referenced by package imports in ``sources``."""
    planned: dict[str, str] = {}
    for source in sources:
        for match in _IMPORT_RE.finditer(source):
            pkg, rel = match.group(1), match.group(2)
            if pkg != package or not rel.startswith("widgets/"):
                continue
            lib_path = f"lib/{rel}.dart"
            file_path = project_dir / lib_path
            if file_path.is_file() and lib_path not in planned:
                planned[lib_path] = file_path.read_text(encoding="utf-8")
    return planned


def refresh_reference_from_lib(project_dir: Path, *, feature_name: str) -> Path:
    """Build reference bundle from ``lib/generated`` layout + canonical screen stub.

    Does not copy ``lib/features/*_screen.dart`` when it contains inlined IR dumps.

    Args:
        project_dir: Flutter project root.
        feature_name: Screen feature slug.

    Returns:
        Path to the written bundle file.
    """
    package = read_pubspec_name(project_dir)
    screen_path = screen_file_path(feature_name)
    layout_path = f"lib/generated/{feature_name}_layout.dart"
    layout_src = (project_dir / layout_path).read_text(encoding="utf-8")
    screen_src = _canonical_screen_source(feature_name, package_name=package)
    planned: dict[str, str] = {
        layout_path: layout_src,
        screen_path: screen_src,
    }
    planned.update(_collect_widget_planned_files(package, project_dir, [layout_src, screen_src]))

    bundle = build_planned_dart_bundle(
        feature_name=feature_name,
        planned_files=planned,
        package_name=package,
        banner="EMITTER REFERENCE",
        description=(
            "Single-file golden per docs/spec.md — layout from lib/generated, "
            "screen delegates to layout (valid Dart)."
        ),
    )
    if bundle is None:
        msg = f"Could not assemble reference bundle for {feature_name!r}"
        raise ValueError(msg)

    out = project_dir / ".figma_debug/reference" / f"{feature_name}_screen.dart"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(bundle, encoding="utf-8")

    meta = {
        "feature": feature_name,
        "bundle": out.relative_to(project_dir).as_posix(),
        "sources": {
            "processed": f".figma_debug/processed/{feature_name}_layout.json",
            "screenIr": f".figma_debug/ir/{feature_name}_pre_emit.json",
            "libLayout": f"lib/generated/{feature_name}_layout.dart",
            "libScreen": "canonical stub (GeneratedScreenShell + const Layout)",
        },
        "note": "Layout from lib/generated; screen stub is maintained here (not broken lib/features dump).",
    }
    meta_path = out.parent / f"{feature_name}_reference.json"
    meta_path.write_text(json.dumps(meta, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    return out


def main(argv: list[str] | None = None) -> int:
    """CLI entrypoint."""
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--project-dir", type=Path, required=True)
    parser.add_argument("--feature", required=True)
    args = parser.parse_args(argv)
    path = refresh_reference_from_lib(args.project_dir.expanduser().resolve(), feature_name=args.feature)
    print(path.as_posix())
    return 0


if __name__ == "__main__":
    sys.exit(main())
