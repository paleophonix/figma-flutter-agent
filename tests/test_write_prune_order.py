"""Write-stage ordering: disk prune follows successful commit."""

from __future__ import annotations

from pathlib import Path
from unittest.mock import MagicMock, patch

from figma_flutter_agent.generator.writing.models import WriteBatch, WriteRecord
from figma_flutter_agent.schemas import AssetManifest
from figma_flutter_agent.stages.write import WriteStageRequest, commit_planned_files


def test_disk_prune_runs_after_successful_write_commit(tmp_path: Path) -> None:
    project_dir = tmp_path / "app"
    widgets = project_dir / "lib" / "widgets"
    widgets.mkdir(parents=True)
    stale = widgets / "stale_cluster_widget.dart"
    stale.write_text(
        "// Generated by figma-flutter-agent\nclass StaleClusterWidget extends StatelessWidget {}",
        encoding="utf-8",
    )
    files = {
        "lib/widgets/keep_widget.dart": """
class KeepWidget extends StatelessWidget {
  const KeepWidget({super.key});
  @override
  Widget build(BuildContext context) => const SizedBox();
}
""",
    }
    order: list[str] = []

    class FakeWriter:
        def __init__(self, *_args, **_kwargs) -> None:
            pass

        def write_files(self, payload):
            order.append("write")
            return WriteBatch(
                backup_dir=tmp_path / "backup",
                written=[
                    WriteRecord(target=project_dir / path, existed_before=False) for path in payload
                ],
            )

        def rollback_batch(self, _batch) -> None:
            order.append("rollback")

        def commit_batch(self, _batch) -> None:
            order.append("commit")

    def _prune_aliases(_project_dir, _planned):
        order.append("prune_aliases")
        return []

    def _prune_unplanned(_project_dir, _planned):
        order.append("prune_unplanned")
        return []

    with (
        patch("figma_flutter_agent.stages.write.DartWriter", FakeWriter),
        patch(
            "figma_flutter_agent.generator.planned.reconcile.prune_disk_widget_stem_aliases",
            side_effect=_prune_aliases,
        ),
        patch(
            "figma_flutter_agent.generator.planned.reconcile.prune_disk_unplanned_agent_widgets",
            side_effect=_prune_unplanned,
        ),
        patch(
            "figma_flutter_agent.stages.write.analyze_planned_dart_files",
            return_value=MagicMock(skipped=True, passed=True, detail="skipped"),
        ),
        patch(
            "figma_flutter_agent.generator.planned.reconcile.imports.find_stale_widget_package_imports",
            return_value=[],
        ),
        patch("figma_flutter_agent.stages.write.update_pubspec", return_value=None),
        patch("figma_flutter_agent.stages.write.commit_pubspec_batch"),
        patch("figma_flutter_agent.stages.write.pubspec_digest", return_value="same"),
    ):
        commit_planned_files(
            WriteStageRequest(
                project_dir=project_dir,
                files_to_write=files,
                asset_manifest=AssetManifest(),
                routing_type="none",
                state_management_type="none",
                package_name="demo",
                emit_parse_gate=False,
                enable_backup=False,
                analyze_scope="written_only",
            )
        )

    assert order.index("write") < order.index("commit")
    assert order.index("commit") < order.index("prune_aliases")
    assert order.index("prune_aliases") < order.index("prune_unplanned")
