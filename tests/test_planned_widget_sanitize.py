"""Planned widget Dart sanitization and reconcile guards."""

from __future__ import annotations

from figma_flutter_agent.generator.dart.llm_codegen import validate_dart_delimiters
from figma_flutter_agent.generator.dart.syntax_repairs import (
    repair_planned_dart_delimiters_if_needed,
    sanitize_planned_widget_syntax,
)
from figma_flutter_agent.generator.planned.reconcile import (
    _balance_planned_widget_delimiters,
    hydrate_planned_widget_files_from_project,
)


def test_strip_duplicate_semicolons() -> None:
    broken = "void main() { runApp(const MaterialApp(home: SizedBox.shrink()));; }\n"
    fixed = sanitize_planned_widget_syntax(broken)
    assert "));" in fixed
    assert "));;" not in fixed


def test_repair_if_needed_keeps_valid_source() -> None:
    source = (
        "import 'package:flutter/material.dart';\n"
        "class Foo extends StatelessWidget {\n"
        "  const Foo({super.key});\n"
        "  @override\n"
        "  Widget build(BuildContext context) => const SizedBox.shrink();\n"
        "}\n"
    )
    assert repair_planned_dart_delimiters_if_needed(source) == source


def test_balance_does_not_break_valid_widget() -> None:
    source = (
        "// <auto-generated>\n"
        "// Generated by figma-flutter-agent. Do not edit by hand.\n"
        "// </auto-generated>\n\n"
        "import 'package:flutter/material.dart';\n\n"
        "class Group17Widget extends StatelessWidget {\n"
        "  const Group17Widget({super.key});\n"
        "  @override\n"
        "  Widget build(BuildContext context) => const SizedBox.shrink();\n"
        "}\n"
    )
    planned = {"lib/widgets/group17_widget.dart": source}
    balanced = _balance_planned_widget_delimiters(planned)
    assert validate_dart_delimiters(balanced["lib/widgets/group17_widget.dart"]) is None
    assert ";\n]))" not in balanced["lib/widgets/group17_widget.dart"]


def test_hydrate_skips_invalid_disk_widget(tmp_path) -> None:
    widget_dir = tmp_path / "lib" / "widgets"
    widget_dir.mkdir(parents=True)
    bad = (
        "// <custom-code>\n// </custom-code>\n"
        "import 'package:flutter/material.dart'\n"
        "class Broken extends StatelessWidget {\n"
        "  @override Widget build(BuildContext c) => const SizedBox();\n"
        "}\n"
    )
    (widget_dir / "broken_widget.dart").write_text(bad, encoding="utf-8")
    planned = {
        "lib/features/x/x_screen.dart": "class XScreen extends StatelessWidget { "
        "const XScreen({super.key}); @override Widget build(BuildContext c) => "
        "const Broken(); }",
    }
    out = hydrate_planned_widget_files_from_project(planned, tmp_path)
    assert "lib/widgets/broken_widget.dart" not in out
