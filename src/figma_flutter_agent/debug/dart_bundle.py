"""Assemble screen, layout, and widget Dart into a single debug bundle file."""

from __future__ import annotations

import re
from pathlib import Path
from typing import Literal

from loguru import logger

from figma_flutter_agent.debug.paths import dart_debug_snapshot_path
from figma_flutter_agent.generator.paths import Architecture, screen_file_path

DartDebugSnapshot = Literal["plan", "final", "bug"]

_IMPORT_RE = re.compile(r"^import\s+['\"](?P<uri>[^'\"]+)['\"]\s*;\s*$", re.MULTILINE)
_SECTION_BEGIN_RE = re.compile(r"^// --- begin (?P<path>.+?) ---\s*$", re.MULTILINE)
_CLASS_RE = re.compile(r"^class (\w+)", re.MULTILINE)
_SCREEN_SHELL_CLASSES = frozenset({"GeneratedScreenShell"})
_SKIP_HEADER_RE = re.compile(
    r"^//\s*(<auto-generated>|</auto-generated>|Generated by figma-flutter-agent|<custom-code>|</custom-code>)",
)


def _uri_to_lib_path(uri: str, *, package_name: str) -> str | None:
    prefix = f"package:{package_name}/"
    if uri.startswith(prefix):
        return f"lib/{uri.removeprefix(prefix)}"
    return None


def _collect_import_uris(source: str) -> list[str]:
    return [match.group("uri") for match in _IMPORT_RE.finditer(source)]


def _collect_bundle_paths(
    *,
    screen_path: str,
    planned_files: dict[str, str],
    package_name: str,
) -> tuple[list[str], list[str]]:
    """Return widget/layout paths to inline and the screen path."""
    pending = [screen_path]
    seen: set[str] = set()
    widget_paths: list[str] = []
    layout_paths: list[str] = []

    while pending:
        path = pending.pop(0)
        if path in seen:
            continue
        seen.add(path)
        source = planned_files.get(path)
        if source is None:
            continue
        for uri in _collect_import_uris(source):
            lib_path = _uri_to_lib_path(uri, package_name=package_name)
            if lib_path is None or lib_path not in planned_files:
                continue
            if lib_path.startswith("lib/widgets/"):
                if lib_path not in widget_paths:
                    widget_paths.append(lib_path)
                pending.append(lib_path)
            elif lib_path.startswith("lib/generated/") and lib_path.endswith("_layout.dart"):
                if lib_path not in layout_paths:
                    layout_paths.append(lib_path)
                pending.append(lib_path)

    widget_paths.sort()
    layout_paths.sort()
    return widget_paths, layout_paths


def _collect_external_imports(
    paths: list[str],
    planned_files: dict[str, str],
    *,
    inlined_paths: set[str],
    package_name: str,
) -> list[str]:
    imports: list[str] = []
    seen: set[str] = set()
    for path in paths:
        source = planned_files.get(path, "")
        for uri in _collect_import_uris(source):
            lib_path = _uri_to_lib_path(uri, package_name=package_name)
            if lib_path in inlined_paths:
                continue
            if uri in seen:
                continue
            seen.add(uri)
            imports.append(f"import '{uri}';")
    return imports


def _strip_file_preamble(source: str) -> str:
    lines: list[str] = []
    for line in source.splitlines():
        stripped = line.strip()
        if not stripped:
            if lines and lines[-1] != "":
                lines.append("")
            continue
        if _IMPORT_RE.match(stripped):
            continue
        if _SKIP_HEADER_RE.match(stripped):
            continue
        if stripped == "// Do not edit by hand.":
            continue
        lines.append(line)
    while lines and lines[0] == "":
        lines.pop(0)
    while lines and lines[-1] == "":
        lines.pop()
    return "\n".join(lines)


def parse_planned_dart_bundle(source: str) -> tuple[list[str], dict[str, str]]:
    """Parse a single-file debug bundle back into per-file bodies.

    Args:
        source: Contents of ``.figma_debug/dart/*`` or ``reference/*`` bundle.

    Returns:
        External import lines (shared package/theme imports) and map of lib-relative
        paths to stripped section bodies (without per-file imports).
    """
    lines = source.splitlines()
    while lines and lines[0].startswith("// Snapshot:"):
        lines.pop(0)
    text = "\n".join(lines)
    first_section = _SECTION_BEGIN_RE.search(text)
    header = text[: first_section.start()] if first_section else text
    external_imports = [
        line
        for line in header.splitlines()
        if _IMPORT_RE.match(line.strip())
    ]
    sections: dict[str, str] = {}
    for match in _SECTION_BEGIN_RE.finditer(text):
        path = match.group("path").strip()
        start = match.end() + 1
        end_marker = f"// --- end {path} ---"
        end_idx = text.find(end_marker, start)
        body = text[start:end_idx] if end_idx >= 0 else text[start:]
        sections[path] = body.strip()
    return external_imports, sections


def planned_files_from_dart_bundle(
    source: str,
    *,
    package_name: str,
) -> dict[str, str]:
    """Rebuild planned project files from a debug/reference Dart bundle.

    Args:
        source: Single-file bundle text produced by :func:`build_planned_dart_bundle`.
        package_name: Pubspec package name for ``import 'package:...'`` reconstruction.

    Returns:
        Map of project-relative paths (``lib/...``) to compilable Dart sources.
    """
    external_imports, sections = parse_planned_dart_bundle(source)
    if not sections:
        msg = "Debug bundle contains no // --- begin lib/... --- sections."
        raise ValueError(msg)

    def _bundle_section_order(path: str) -> tuple[int, str]:
        if path.startswith("lib/widgets/"):
            return (0, path)
        if path.startswith("lib/generated/"):
            return (1, path)
        return (2, path)

    ordered_paths = sorted(sections, key=_bundle_section_order)
    planned: dict[str, str] = {}
    prior: list[tuple[str, str]] = []
    for path in ordered_paths:
        body = sections[path]
        imports = list(external_imports)
        seen = set(imports)
        for prior_path, prior_body in prior:
            if not _section_references_prior(body, prior_body):
                continue
            uri = f"package:{package_name}/{prior_path.removeprefix('lib/')}"
            import_line = f"import '{uri}';"
            if import_line not in seen:
                seen.add(import_line)
                imports.append(import_line)
        preamble = [
            "// <auto-generated>",
            "// Do not edit by hand.",
            "",
            *imports,
        ]
        if imports:
            preamble.append("")
        planned[path] = "\n".join([*preamble, body, ""])
        prior.append((path, body))
    return planned


def detect_screen_class_from_planned_files(
    planned_files: dict[str, str],
    *,
    feature_name: str,
    architecture: Architecture = "feature_first",
) -> str:
    """Return the primary screen widget class from planned files."""
    from figma_flutter_agent.parser.navigation import _screen_class_name

    screen_path = screen_file_path(feature_name, architecture=architecture)
    screen_source = planned_files.get(screen_path, "")
    expected = _screen_class_name(feature_name)
    declared = _CLASS_RE.findall(screen_source)
    if expected in declared:
        return expected
    for class_name in declared:
        if class_name in _SCREEN_SHELL_CLASSES:
            continue
        if class_name.endswith("Screen"):
            return class_name
    return expected


def _section_references_prior(body: str, prior_body: str) -> bool:
    for class_name in _CLASS_RE.findall(prior_body):
        if re.search(rf"\b{re.escape(class_name)}\b", body):
            return True
    return False


def build_planned_dart_bundle(
    *,
    feature_name: str,
    planned_files: dict[str, str],
    package_name: str,
    architecture: Architecture = "feature_first",
    banner: str = "DEBUG BUNDLE",
    description: str = (
        "Single-file view of screen + inlined widgets/layout from this generation run."
    ),
) -> str | None:
    """Build a single-file Dart bundle from planned project files.

    Args:
        feature_name: Resolved screen feature slug.
        planned_files: Planned project files keyed by relative path.
        package_name: Flutter package name from ``pubspec.yaml``.
        architecture: Project layout mode.
        banner: First-line bundle label (e.g. ``DEBUG BUNDLE``, ``EMITTER REFERENCE``).
        description: Human-readable bundle purpose comment.

    Returns:
        Bundled Dart source, or ``None`` when the screen file is missing.
    """
    screen_path = screen_file_path(feature_name, architecture=architecture)
    screen_source = planned_files.get(screen_path)
    if screen_source is None:
        return None

    widget_paths, layout_paths = _collect_bundle_paths(
        screen_path=screen_path,
        planned_files=planned_files,
        package_name=package_name,
    )
    inlined_paths = set(widget_paths) | set(layout_paths)
    ordered_paths = [*widget_paths, *layout_paths, screen_path]
    imports = _collect_external_imports(
        ordered_paths,
        planned_files,
        inlined_paths=inlined_paths,
        package_name=package_name,
    )

    sections: list[str] = [
        f"// {banner} — not for compilation.",
        f"// Feature: {feature_name}",
        f"// {description}",
        "// Generated by figma-flutter-agent.",
        "",
        *imports,
        "",
    ]

    for path in ordered_paths:
        body = _strip_file_preamble(planned_files.get(path, ""))
        if not body:
            continue
        sections.extend(
            [
                f"// --- begin {path} ---",
                body,
                f"// --- end {path} ---",
                "",
            ]
        )

    return "\n".join(sections).rstrip() + "\n"


def build_dart_debug_bundle(
    *,
    feature_name: str,
    planned_files: dict[str, str],
    package_name: str,
    architecture: Architecture = "feature_first",
) -> str | None:
    """Build a single-file Dart bundle for screen debugging.

    Args:
        feature_name: Resolved screen feature slug.
        planned_files: Final planned project files keyed by relative path.
        package_name: Flutter package name from ``pubspec.yaml``.
        architecture: Project layout mode.

    Returns:
        Bundled Dart source, or ``None`` when the screen file is missing.
    """
    return build_planned_dart_bundle(
        feature_name=feature_name,
        planned_files=planned_files,
        package_name=package_name,
        architecture=architecture,
    )


def write_dart_debug_snapshot(
    project_dir: Path,
    *,
    feature_name: str,
    planned_files: dict[str, str],
    package_name: str,
    architecture: Architecture = "feature_first",
    snapshot: DartDebugSnapshot = "final",
) -> Path | None:
    """Write a debug Dart bundle snapshot for ``feature_name``.

    Args:
        project_dir: Flutter project root.
        feature_name: Resolved screen feature slug.
        planned_files: Planned project files keyed by relative path.
        package_name: Flutter package name from ``pubspec.yaml``.
        architecture: Project layout mode.
        snapshot: ``plan`` after plan stage, ``final`` before write, ``bug`` on gate failure.

    Returns:
        Path to the written bundle, or ``None`` when the screen is missing.
    """
    bundle = build_dart_debug_bundle(
        feature_name=feature_name,
        planned_files=planned_files,
        package_name=package_name,
        architecture=architecture,
    )
    if bundle is None:
        return None
    path = dart_debug_snapshot_path(project_dir, feature_name, snapshot)
    path.parent.mkdir(parents=True, exist_ok=True)
    header = (
        f"// Snapshot: {snapshot}\n"
        if snapshot != "final"
        else ""
    )
    path.write_text(header + bundle, encoding="utf-8")
    logger.info("Saved debug Dart snapshot ({}) to {}", snapshot, path.as_posix())
    return path


def write_dart_debug_bundle(
    project_dir: Path,
    *,
    feature_name: str,
    planned_files: dict[str, str],
    package_name: str,
    architecture: Architecture = "feature_first",
) -> Path | None:
    """Write the final pre-write debug Dart bundle for ``feature_name``."""
    return write_dart_debug_snapshot(
        project_dir,
        feature_name=feature_name,
        planned_files=planned_files,
        package_name=package_name,
        architecture=architecture,
        snapshot="final",
    )
