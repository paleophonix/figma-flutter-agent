import 'rules_strings.dart' show stripBareUnicodeEscapesOutsideLiterals;

final _dartPackageImportRe = RegExp(
  r'''^\s*import\s+(?:'([^']*)'|"([^"]*)")\s*;''',
);
final _malformedDirectiveRe = RegExp(
  r"^\s*(?:import|export)\s+(?!\s*[\u0027\u0022])",
);
final _autoGeneratedBlockRe = RegExp(
  r'//\s*<auto-generated>\s*\n.*?//\s*</auto-generated>\s*\n?',
  dotAll: true,
  caseSensitive: false,
);

String stripInvalidDartImports(String source) {
  final kept = <String>[];
  for (final line in source.split(RegExp(r'\r?\n'))) {
    final stripped = line.trim();
    if (stripped.isEmpty) {
      kept.add(line);
      continue;
    }
    if (_malformedDirectiveRe.hasMatch(stripped)) {
      continue;
    }
    if (stripped.startsWith('import ') || stripped.startsWith('export ')) {
      final match = _dartPackageImportRe.firstMatch(stripped);
      if (match == null) {
        continue;
      }
      final uri = match.group(1) ?? match.group(2) ?? '';
      if (uri.isEmpty) {
        continue;
      }
      if (!_isAscii(uri) || uri.contains(r'\u')) {
        continue;
      }
    }
    kept.add(line);
  }
  return kept.join('\n');
}

bool _isAscii(String value) {
  for (final unit in value.codeUnits) {
    if (unit > 127) {
      return false;
    }
  }
  return true;
}

String stripEmbeddedAutoGeneratedMarkers(String source) {
  return source.replaceAll(_autoGeneratedBlockRe, '').trim();
}

String dedupeDartImportLines(String source) {
  final seen = <String>{};
  final kept = <String>[];
  for (final line in source.split(RegExp(r'\r?\n'))) {
    final stripped = line.trim();
    if (stripped.startsWith('import ') || stripped.startsWith('export ')) {
      if (seen.contains(stripped)) {
        continue;
      }
      seen.add(stripped);
    }
    kept.add(line);
  }
  return kept.join('\n');
}

String _insertImportAfterMaterial(String updated, String importLine) {
  const material = "import 'package:flutter/material.dart';";
  if (updated.contains(material)) {
    return updated.replaceFirst(material, '$material\n$importLine');
  }
  return updated.isEmpty ? importLine : '$importLine\n\n$updated';
}

String ensureBaseScreenImports(String source) {
  var updated = source;
  if (!updated.contains('package:flutter/material.dart')) {
    updated = _insertImportAfterMaterial(
      updated,
      "import 'package:flutter/material.dart';",
    );
  }
  if (!updated.contains('package:flutter/gestures.dart') &&
      RegExp(r'\b(?:TapGestureRecognizer|LongPressGestureRecognizer|GestureRecognizer)\b')
          .hasMatch(updated)) {
    updated = _insertImportAfterMaterial(
      updated,
      "import 'package:flutter/gestures.dart';",
    );
  }
  if (!updated.contains('package:flutter_svg/flutter_svg.dart') &&
      RegExp(r'\b(?:SvgPicture|SvgAssetLoader)\b').hasMatch(updated)) {
    updated = _insertImportAfterMaterial(
      updated,
      "import 'package:flutter_svg/flutter_svg.dart';",
    );
  }
  return updated;
}

String ensureAppColorsImport(String source, {String packageName = 'demo_app'}) {
  if (!RegExp(r'\bAppColors\.').hasMatch(source)) {
    return source;
  }
  if (RegExp(r'theme/app_colors\.dart').hasMatch(source)) {
    return source;
  }
  var importLine = "import 'package:$packageName/theme/app_colors.dart';";
  final themeMatch = RegExp(r"import\s+'package:([^/]+)/theme/").firstMatch(source);
  if (themeMatch != null) {
    final pkg = themeMatch.group(1);
    if (pkg != null && pkg != 'flutter') {
      importLine = "import 'package:$pkg/theme/app_colors.dart';";
    }
  } else {
    for (final match in RegExp(r"import\s+'package:([^/]+)/[^']+';").allMatches(source)) {
      final pkg = match.group(1);
      if (pkg != null && pkg != 'flutter') {
        importLine = "import 'package:$pkg/theme/app_colors.dart';";
        break;
      }
    }
  }
  return _insertImportAfterMaterial(source, importLine);
}

String sanitizeImportsPass(String source) {
  var updated = stripBareUnicodeEscapesOutsideLiterals(source);
  updated = stripInvalidDartImports(updated);
  updated = stripEmbeddedAutoGeneratedMarkers(updated);
  updated = dedupeDartImportLines(updated);
  updated = ensureBaseScreenImports(updated);
  updated = ensureAppColorsImport(updated);
  return updated;
}
